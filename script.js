
$(document).ready(function() {
  $(window).scroll(function() {
    if($(document).scrollTop() > 180 && $(document).scrollTop()<2200) {
      $('.planet').addClass('planet-move');
    }
   
    else {
    $('.planet').removeClass('planet-move');
    }
  });
});

